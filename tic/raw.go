//00000000 TickItem        struc ; (sizeof=0x14, variable size)
//00000000 dateTime        dd ?
//00000004 count           dw ?
//00000006 volumeOffset    dw ?                    ; base 16
//00000008 unknown         dw ?                    ; base 16
//0000000A type            dw ?
//0000000C price           dd ?
//00000010 volume          dd ?
//00000014 tickDetail      db 0 dup(?)             ; struct  offset
//00000014 TickItem        ends
type TickItem struct {
	DateTime  uint32
	Count     uint16    // ((WORD)+1)*16作为解压后数据的大小
	VolOffset uint16    // 成交量的偏移地址
	VolSize   uint16
	Type      uint16    // 交易方向0=buy;1=sell;2=unknown
	Price     uint32    // 成交价格
	Volume    uint32    // 成交量
	// TickItemDetail
}

//00000000 StockTick       struc ; (sizeof=0x14, variable size)
//00000000 market          db ?
//00000001 code            db 7 dup(?)
//00000008 dateTime        dd ?
//0000000C tickSize        dd ?
//00000010 unknown         dd ?
//00000014 tickItem        db 0 dup(?)
//00000014 StockTick       ends
type StockTick struct {
	Market   byte
	Code     [7]byte
	Date     uint32
	TickSize uint32
	Unknown  uint32
	// Item     TickItem
}

//00000000 TickFileData    struc ; (sizeof=0x2, variable size)
//00000000 stockCount      dw ?
//00000002 stockTick       db 0 dup(?)             ; struct  offset
//00000002 TickFileData    ends
type TickFileData struct {
	StockCount  uint16
	// StockTickList []StockTick
}

